#!/bin/bash

# Font Configuration
# This file centralizes all font definitions used across the dotfiles

# Primary Font Face - JetBrainsMono Nerd Font
FONT="JetBrainsMono Nerd Font"
FONT_FACE="JetBrainsMono Nerd Font"

# Font definitions for different uses
# JetBrainsMono variants
FONT_JETBRAINS_BLACK_12="${FONT}:Black:12.0"
FONT_JETBRAINS_MEDIUM_12="${FONT}:Medium:12.0"
FONT_JETBRAINS_BOLD_15="${FONT}:Bold:15.0"
FONT_JETBRAINS_BOLD_20="${FONT}:Bold:20.0"
FONT_JETBRAINS_BOLD_22="${FONT}:Bold:22.0"

# Hack Nerd Font variants
FONT_HACK_BOLD_17="Hack Nerd Font:Bold:17.0"
FONT_HACK_ITALIC_13="Hack Nerd Font:Italic:13.0"

# SF Pro variants
FONT_SF_PRO_SEMIBOLD_17="SF Pro:Semibold:17.0"

# Sketchybar App Font
FONT_SKETCHYBAR_APP_16="sketchybar-app-font:Regular:16.0"

# Export all font variables
export FONT
export FONT_FACE
export FONT_JETBRAINS_BLACK_12
export FONT_JETBRAINS_MEDIUM_12
export FONT_JETBRAINS_BOLD_15
export FONT_JETBRAINS_BOLD_20
export FONT_JETBRAINS_BOLD_22
export FONT_HACK_BOLD_17
export FONT_HACK_ITALIC_13
export FONT_SF_PRO_SEMIBOLD_17
export FONT_SKETCHYBAR_APP_16
